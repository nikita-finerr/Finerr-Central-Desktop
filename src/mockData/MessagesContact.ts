const resolveChatKind = (item: TimelineItem): ChatItemKind => {
  if (item.type === "SMS") {
    if (/image shared/i.test(item.preview)) return "Image";
    return "SMS";
  }
  return item.type;
};

const isChatMessageKind = (
  kind: ChatItemKind,
): kind is Extract<ChatItemKind, "SMS" | "Image"> =>
  kind === "SMS" || kind === "Image";

const contentForItem = (
  item: TimelineItem,
  kind: Extract<ChatItemKind, "SMS" | "Image">,
): string => {
  if (kind === "SMS") return item.preview;
  const name = item.preview.match(
    /[\w][\w\s._-]*\.(?:png|jpe?g|gif|webp)/i,
  )?.[0];
  return name ?? "Photo attachment";
};

const timelineToChatItems = (timeline: TimelineItem[]): ChatItem[] => {
  const chronological = [...timeline]
    .reverse()
    .filter((item) => isChatMessageKind(resolveChatKind(item)));

  return chronological.map((item, index) => {
    const kind = resolveChatKind(item);
    if (!isChatMessageKind(kind)) {
      throw new Error(`Unexpected chat kind: ${kind}`);
    }

    const chatItem: ChatItem = {
      id: item.id,
      kind,
      direction: item.direction,
      timestamp: item.timestamp,
      sortKey: index + 1,
      content: contentForItem(item, kind),
    };

    if (kind === "Image") {
      chatItem.imageMeta = {
        images: [
          {
            uri: `https://picsum.photos/seed/${encodeURIComponent(item.id)}/900/700`,
            width: 900,
            height: 700,
            fileName:
              chatItem.content !== "Photo attachment"
                ? chatItem.content
                : "Clinical photo.jpg",
            size: 245_760,
          },
        ],
      };
    }

    return chatItem;
  });
};

