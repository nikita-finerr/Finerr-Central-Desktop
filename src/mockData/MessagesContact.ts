export type CommType = "SMS" | "Call" | "Fax" | "Voicemail";

export type ChatItemKind = CommType | "File" | "Image";

export type FaxPriority = "normal" | "urgent";

export interface FaxMeta {
  documentName: string;
  documentUri?: string;
  pageCount: number;
  fileSize?: number;
  recipientName: string;
  faxNumber: string;
  coverSheet: boolean;
  priority: FaxPriority;
  sentLabel: string;
  deliveryStatus?: "delivered" | "pending" | "failed";
}

export interface ChatImage {
  uri: string;
  width?: number;
  height?: number;
  fileName?: string;
  size?: number;
}

export interface ImageMeta {
  images: ChatImage[];
}

export interface FileMeta {
  name: string;
  uri: string;
  mimeType?: string;
  size?: number;
}

export interface ChatItem {
  id: string;
  kind: ChatItemKind;
  direction: "inbound" | "outbound";
  timestamp: string;
  timeLabel?: string;
  dateLabel?: string;
  sortKey: number;
  content: string;
  activityTitle?: string;
  internalNote?: string;
  faxMeta?: FaxMeta;
  imageMeta?: ImageMeta;
  fileMeta?: FileMeta;
  isRead?: boolean;
}

export interface TimelineItem {
  id: string;
  type: CommType;
  timestamp: string;
  preview: string;
  direction: "inbound" | "outbound";
}

export interface ContactDetail {
  conversationId: string;
  phone: string;
  email: string;
  notes: string;
  patientId?: string;
  timeline: TimelineItem[];
}

const detail = (
  id: string,
  phone: string,
  email: string,
  notes: string,
  timeline: TimelineItem[],
  patientId?: string,
): ContactDetail => {
  return { conversationId: id, phone, email, notes, timeline, patientId };
};

